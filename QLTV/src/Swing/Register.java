    public boolean insertAccountAndCustomer() {
        // L·∫•y th√¥ng tin t√†i kho·∫£n
        String UserName = jTextFieldUser1.getText().trim();
        String PassWord = jPasswordField2.getText().trim();
        String ConfirmPassword = jPasswordField3.getText().trim();

        // L·∫•y th√¥ng tin kh√°ch h√†ng
        String Name = TextName.getText().trim();
        String SSN = jFormattedTextSSN.getText().trim();
        String birthDateStr = null;
        try {
            Date selectedDate = jCusDate.getDate(); // L·∫•y ng√†y t·ª´ JDateChooser
            if (selectedDate != null) {
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd"); // ƒê·ªãnh d·∫°ng mong mu·ªën
                 birthDateStr = dateFormat.format(selectedDate); // Chuy·ªÉn Date th√†nh String
            } else {
                
                JOptionPane.showMessageDialog(rootPane, "you are do not input BirthDate", "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        String gender = Genger();
        String PhoneNumber = jFormattedTextPhone.getText().trim();
        String Email = EmailText.getText().trim();
        String Address = jTextAddress.getText().trim();

        // Ki·ªÉm tra th√¥ng tin t√†i kho·∫£n v√† kh√°ch h√†ng ƒë·∫ßy ƒë·ªß
        if (UserName.isEmpty() || PassWord.isEmpty() || ConfirmPassword.isEmpty()
                || Name.isEmpty() || SSN.isEmpty() || birthDateStr.isEmpty() || gender.isEmpty()
                || PhoneNumber.isEmpty() || Email.isEmpty() || Address.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß th√¥ng tin t√†i kho·∫£n v√† kh√°ch h√†ng!", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Ki·ªÉm tra t√†i kho·∫£n h·ª£p l·ªá
        if (UserName.length() < 5) {
            JOptionPane.showMessageDialog(null, "T√™n t√†i kho·∫£n ph·∫£i c√≥ √≠t nh·∫•t 5 k√Ω t·ª±!", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!PassWord.equals(ConfirmPassword)) {
            JOptionPane.showMessageDialog(null, "M·∫≠t kh·∫©u kh√¥ng kh·ªõp! Vui l√≤ng nh·∫≠p l·∫°i.", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (PassWord.length() < 6) {
            JOptionPane.showMessageDialog(null, "M·∫≠t kh·∫©u ph·∫£i c√≥ √≠t nh·∫•t 6 k√Ω t·ª±!", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Ki·ªÉm tra ng√†y sinh h·ª£p l·ªá
        LocalDate BirthDate;
        try {
            BirthDate = LocalDate.parse(BirthDateStr);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ng√†y sinh kh√¥ng h·ª£p l·ªá! (ƒê·ªãnh d·∫°ng YYYY-MM-DD)", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Ki·ªÉm tra SSN h·ª£p l·ªá (9 ho·∫∑c 12 s·ªë)
        if (!SSN.matches("\\d{9}|\\d{12}")) {
            JOptionPane.showMessageDialog(null, "SSN ph·∫£i c√≥ 9 ho·∫∑c 12 ch·ªØ s·ªë!", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Ki·ªÉm tra Email h·ª£p l·ªá
        if (!Email.matches("^[\\w.-]+@[a-zA-Z\\d.-]+\\.[a-zA-Z]{2,6}$")) {
            JOptionPane.showMessageDialog(null, "Email kh√¥ng h·ª£p l·ªá!", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Ki·ªÉm tra s·ªë ƒëi·ªán tho·∫°i h·ª£p l·ªá (9-10 s·ªë)
        if (!PhoneNumber.matches("\\d{9,10}")) {
            JOptionPane.showMessageDialog(null, "S·ªë ƒëi·ªán tho·∫°i kh√¥ng h·ª£p l·ªá! Ph·∫£i c√≥ 9-10 ch·ªØ s·ªë.", "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // üîπ **T·∫°o AccountId duy nh·∫•t**
        int newAccountId = accSer.increaAcc() + 1;

        // T·∫°o t√†i kho·∫£n
        Account acc = new Account(newAccountId, UserName, PassWord);
        try {
            accSer.insert(acc);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "L·ªói khi t·∫°o t√†i kho·∫£n: " + e.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false; // N·∫øu l·ªói, d·ª´ng l·∫°i
        }

        // üîπ **T·∫°o CusID duy nh·∫•t**
        String CusID = CusSer.increaseCUSID(); // T·∫°o CusID m·ªõi (kh√¥ng tr√πng l·∫∑p)

        double totalPayment = 0.0;
        Customer cus = new Customer(CusID, Name, SSN, BirthDate, gender, PhoneNumber, Email, Address, totalPayment, newAccountId);

        try {
            CusSer.insert(cus);
            JOptionPane.showMessageDialog(null, "T√†i kho·∫£n v√† kh√°ch h√†ng ƒë√£ ƒë∆∞·ª£c ƒëƒÉng k√Ω th√†nh c√¥ng!", "Th√¥ng b√°o", JOptionPane.INFORMATION_MESSAGE);
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "L·ªói khi ƒëƒÉng k√Ω kh√°ch h√†ng: " + e.getMessage(), "L·ªói", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

